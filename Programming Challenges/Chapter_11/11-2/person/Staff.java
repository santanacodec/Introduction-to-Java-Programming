package person;

/**
 *
 * @author Edgar Santana
 */
public class Staff extends Employee {
    String title;
    
    public Staff(String title) {
        this.title = title;
    }
    
    public String getTitle() {
        return title;
    }
    
    public void setTitle(String title) {
        this.title = title;
    }
    
    @Override
    public String toString() {
        return "Your staff title is: " + title;
    }
}
